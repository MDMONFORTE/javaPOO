public class FloatVSDoubleVSLong {
    public static void main(String[] args) {
        double numeroDecimal = 5.0;
        float otroDecimal = 5.0f;
        long poblacionMundial = 5000000000000000L;

        System.out.println(numeroDecimal);
        System.out.println(otroDecimal);
        System.out.println(poblacionMundial);
    }
}
